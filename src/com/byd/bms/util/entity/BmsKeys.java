package com.byd.bms.util.entity;

public class BmsKeys {
	private int id;
	private String key_name;
	private String key_name_en;
	private String value;
	
	public String getKey_name() {
		return key_name;
	}
	public void setKey_name(String key_name) {
		this.key_name = key_name;
	}
	public String getKey_name_en() {
		return key_name_en;
	}
	public void setKey_name_en(String key_name_en) {
		this.key_name_en = key_name_en;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}

}
